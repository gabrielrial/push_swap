#include "libft.h"

int	ft_atoi(const char *n)
{
	int	i;
	int	a;
	int	num;
	int	sig;

	sig = 1;
	num = 0;
	i = 0;
	a = 0;
	while (n[i] == 32 || (n[i] >= 9 && n[i] <= 13))
		i++;
	if (n[i] == '-' || n[i] == '+')
	{
		if (n[i] == '-')
			sig = -1;
		i++;
	}
	while (n[i + a] >= '0' && n[i + a] <= '9')
	{
		if ((a > 8 && n[i + a] > '7' && sig == 1) || (a > 8 & n[i + a] > '8'))
			return (0);
		num = num * 10 + (n[i] - '0');
		a++;
	}
	return (num * sig);
}
